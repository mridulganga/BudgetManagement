    private void BTNPaidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNPaidActionPerformed
            
            DefaultTableModel model = (DefaultTableModel) T1.getModel();
            String val = "" + model.getValueAt(T1.getSelectedRow(), 0);
            int due=previousDue(DBC.currentUser,val)-Integer.parseInt(TF1.getText());
            
            Connection con = DBC.ConnectDB();
            if (due==0)
            {
                DBC.ExecuteSQL(con, "Delete from dues where payee='" + DBC.currentUser + "' and tpay='"+val+"'");
            }
            else
            {
                DBC.ExecuteSQL(con, "update dues set amount = " + due +" where payee='" + DBC.currentUser + "' and tpay='"+val+"'");
            }
            
            
            DBC.DisconnectDB(con);
            loadDues();

    }//GEN-LAST:event_BTNPaidActionPerformed

