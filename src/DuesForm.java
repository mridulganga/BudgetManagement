    private void BTNEntryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNEntryActionPerformed
        
        String payee,tpay;
        if (chk_P.isSelected())
        {
             tpay = T_Name.getSelectedItem().toString();
             payee = DBC.currentUser;
        }
        else
        {
             tpay = DBC.currentUser;
             payee = T_Name.getSelectedItem().toString();
        }
        String amount = ""+(Integer.parseInt(T_Amount.getText()) + previousDue(payee,tpay));
        
        String id = (getMaxID()+1)+"";
        String sql = "insert into dues values("+id+",'"+payee+"','"+tpay+"',"+amount+")";
        
        Connection con = DBC.ConnectDB();
        DBC.ExecuteSQL(con, "delete from dues where payee='"+payee+"' and tpay='"+tpay+"'");
        DBC.ExecuteSQL(con, sql);
        loadDues();
        DBC.DisconnectDB(con);
    }//GEN-LAST:event_BTNEntryActionPerformed

    private int previousDue(String payee,String tpay)
    {
        Connection con = DBC.ConnectDB();
        try{
            ResultSet R = DBC.getTable(con, "Select amount from dues where tpay='"+tpay+"' and payee='"+payee+"'");
            R.first();
            int amount =R.getInt("amount");
            return amount;
        }catch(Exception e){return 0;}
    }
    
