    private void loadNames()
    {
              int i=0;
        Connection con = DBC.ConnectDB();
       ResultSet R = DBC.getTable(con, "select Count(*)'count' from members");
       try{
           R.first();
           int count=R.getInt("count");
           R.close();
           
           R = DBC.getTable(con,"select * from members");
           String[] mNames = new String [count];
           while(R.next())
           {
               mNames[i]=R.getString("name");
               i++;
           }
           T_Name.setModel(new javax.swing.DefaultComboBoxModel(mNames));
       }catch(Exception e)
       {
           System.out.println(e);
       }
       DBC.DisconnectDB(con);
       
    }
    
    private void loadDues()
    {
        String payee,amount;
        DefaultTableModel model1 = (DefaultTableModel) T1.getModel();
        model1.setRowCount(0);
        DefaultTableModel model2 = (DefaultTableModel) T2.getModel();
        model2.setRowCount(0);
        
     //  To Pay
        Connection con = DBC.ConnectDB();
        try{
            
        ResultSet R1 = DBC.getTable(con, "select tpay,amount from dues where payee='"+DBC.currentUser+"'");
        while (R1.next())
        {
            payee = R1.getString("tpay");
            amount = R1.getString("amount");
            model1.addRow(new Object[]{payee,amount});
                T1.setModel(model1);
        }
     // To be Paid From
        ResultSet R2 = DBC.getTable(con, "select payee,amount from dues where tpay='"+DBC.currentUser+"'");
        while (R2.next())
        {
            payee = R2.getString("payee");
            amount = R2.getString("amount");
            model2.addRow(new Object[]{payee,amount});
                T2.setModel(model2);
        }
        }catch(Exception e){}
    } // End of LoadDues Method
    
        private int getMaxID()
{
     int maxID=0;
    Connection con = DBC.ConnectDB();
    ResultSet R = DBC.getTable(con, "Select max(id)'id' from dues");
        
    try {
        R.first();
            maxID=R.getInt("id");
            return maxID;
        } catch (Exception e) {
            System.out.println(e);
        }
    
        DBC.DisconnectDB(con);
        return 0;
}
