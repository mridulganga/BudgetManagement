        if (res==0)
        {
            DefaultTableModel model = (DefaultTableModel) T_Trans.getModel();
            String val = "" + model.getValueAt(T_Trans.getSelectedRow(), 0);
            Connection con = DBC.ConnectDB();
            DBC.ExecuteSQL(con, "Delete from transaction where id=" + val);
            DBC.DisconnectDB(con);
            loadTrans();
        }
