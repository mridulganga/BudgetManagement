    private void loadTrans()
    {
        Connection con = DBC.ConnectDB();
        ResultSet R = DBC.getTable(con, "select * from transaction where name ='" + DBC.currentUser+"'");

        DefaultTableModel model = (DefaultTableModel) T_Trans.getModel();
        model.setRowCount(0);
        int W,S;
        try {
            while (R.next()) {
                int id = R.getInt("id");
                String remark = R.getString("remark");
              int ttype = R.getInt("ttype");
                String date = "" + R.getDate("date");
                String sharedby= R.getString("shared");
              int amount = R.getInt("amount");
              if (ttype==1)
              {  W = amount; S=0;}
              else
              { W=0; S=amount;}
              
                
                model.addRow(new Object[]{id, remark, date, sharedby, W, S});
                T_Trans.setModel(model);
            }

        } catch (Exception e) {
            System.out.println(e);
        }

        DBC.DisconnectDB(con);
    }
